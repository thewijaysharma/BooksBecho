package dataModels;

public class contactusdata {


		private String em;
		private String name;
		private int phone;
		private String comp;
		private String msg;


			
		
		public String getEm() {
			return em;
		}
		public void setEm(String em) {
			this.em = em;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public int getPhone() {
			return phone;
		}
		public void setPhone(int phone) {
			this.phone = phone;
		}
		public String getComp() {
			return comp;
		}
		public void setComp(String comp) {
			this.comp = comp;
		}
		
		public String getMsg() {
			return msg;
		}
		public void setMsg(String msg) {
			this.msg = msg;
		}
		
		


}
